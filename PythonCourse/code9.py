# Operadores aritméticos

adicao = 10 + 10
subtracao = 10 - 5
multiplicacao = 10 * 10
divisao_normal = 10 / 2.2
divisao_inteira = 10 // 2.2
exponenciacao = 2 ** 10
modulo = 55 % 2 

print ('Adição', adicao)
print ('Subtração', subtracao)
print ('Multiplicação', multiplicacao)
print ('Divisão', divisao_normal)
print ('Divisão Inteira', divisao_inteira)
print ('Exponenciação', exponenciacao)
print ('Módulo', modulo)

print ('Verificação de Restos')

print ('10 % 8 ?', 10 % 8 == 0)
